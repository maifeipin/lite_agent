@skill(
    name="todo_list",
    description="查询待办事项列表",
    params={
        "status": {"type": "string", "description": "状态过滤，如 pending, active, done, 或逗号分隔", "default": "pending,active"},
        "project": {"type": "string", "description": "按项目过滤"},
        "kind": {"type": "string", "description": "按分类过滤"},
        "limit": {"type": "integer", "description": "限制返回数量", "default": 20}
    }
)
def todo_list(status: str = "pending,active", project: str = None, kind: str = None, limit: int = 20) -> str:
    conn = _conn()
    cursor = conn.cursor()
    
    query = "SELECT id, title, status, kind, project, due_at FROM todos WHERE 1=1"
    params = []
    
    if status and status != 'all':
        statuses = status.split(',')
        query += f" AND status IN ({','.join(['?'] * len(statuses))})"
        params.extend(statuses)
        
    if project:
        query += " AND project = ?"
        params.append(project)
        
    if kind:
        query += " AND kind = ?"
        params.append(kind)
        
    query += " ORDER BY updated_at DESC LIMIT ?"
    params.append(limit)
    
    cursor.execute(query, params)
    rows = cursor.fetchall()
    conn.close()
    
    if not rows:
        return "没有找到符合条件的待办事项。"
        
    result = []
    for r in rows:
        tags = [r[3]]
        if r[4]: tags.append(f"proj:{r[4]}")
        tag_str = f"[{','.join(tags)}]"
        due_str = f" (到期: {r[5]})" if r[5] else ""
        result.append(f"- [{r[2]}] {r[0]} {tag_str} {r[1]}{due_str}")
        
    return "\n".join(result)

